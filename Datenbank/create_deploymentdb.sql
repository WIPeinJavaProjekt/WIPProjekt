create table USERTYPES (
    utid INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    Type VARCHAR(50),
    TechIsActive bit DEFAULT 1,
    TechIsDeleted bit DEFAULT 0,
    TechIsModified TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

