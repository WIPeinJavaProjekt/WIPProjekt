    ALTER TABLE ARTICLEVERSION ADD COLUMN aid int;
    ALTER TABLE ARTICLEVERSION ADD FOREIGN KEY(aid) REFERENCES ARTICLE(aid);

create table ARTICLEIMAGE(
    aimgid INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
