/**
 * JS-file to show or hide the form to change the delivery adress of the current order.
 */

//Function To Display Popup
function div_show() {
	document.getElementById('change-delivery-adr').style.display = "block";
}

//Function to Hide Popup
function div_hide(){
	document.getElementById('change-delivery-adr').style.display = "none";
}
